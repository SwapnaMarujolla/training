//EXAMPLE FOR ARITHMETIC OPERATIONS
let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
console.log(x ** y);// This indicates Exponentiation i.e,x^y

console.log(x++);//This indicates increment and it behaves differently on which side the ++ sign is being added
console.log(x--);// This indicates decrement